public class TypeCasting {
    public static void main(String []args)
    {
    int a=45;
    double b;
    b=a;
    System.out.println(b);

    double num1=45.5;
    int num2;
    num2=(int)num1;
    System.out.println(num2);

    int n1=13;
    int n2=7;
    float result=n1/n2;
    System.out.println(result);

    float m1=13;
    float m2=7;
    float Result_1=m1/m2;
    System.out.println(Result_1);

    }

}
